export const filterListings = (listings, filters) => {
  return listings.filter(listing => {
    // Search filter
    if (filters.search) {
      const searchTerm = filters.search.toLowerCase();
      if (!listing.title.toLowerCase().includes(searchTerm) &&
          !listing.description.toLowerCase().includes(searchTerm)) {
        return false;
      }
    }

    // Category filter
    if (filters.category && filters.category !== '') {
      if (listing.category.toLowerCase() !== filters.category.toLowerCase()) {
        return false;
      }
    }

    // Price range filter
    if (filters.priceRange && filters.priceRange !== '') {
      const [min, max] = filters.priceRange.split('-').map(p => 
        p.includes('+') ? Infinity : parseInt(p) || 0
      );
      if (listing.price < min || listing.price > max) {
        return false;
      }
    }

    // Condition filter
    if (filters.condition && filters.condition !== '') {
      if (listing.condition !== filters.condition) {
        return false;
      }
    }

    // Location filter (simple contains check)
    if (filters.location && filters.location !== '') {
      if (!listing.location.toLowerCase().includes(filters.location.toLowerCase())) {
        return false;
      }
    }

    return true;
  });
};

export const sortListings = (listings, sortBy) => {
  const sorted = [...listings];
  
  switch (sortBy) {
    case 'price-low':
      return sorted.sort((a, b) => a.price - b.price);
    case 'price-high':
      return sorted.sort((a, b) => b.price - a.price);
    case 'newest':
    default:
      return sorted.sort((a, b) => b.id - a.id); // Higher ID = newer
  }
};